class Solution {
public:
    /**
     * http://www.lintcode.com/zh-cn/problem/find-minimum-in-rotated-sorted-array-ii/-160-寻找旋转排序数组中的最小值 II
     * @param num: the rotated sorted array
     * @return: the minimum number in the array
     */
    int findMin(vector<int> &num) {
        // write your code here
        int size = num.size();
        if (size <= 0) {
            return 0;
        }
        if (size == 1) {
            return num[0];
        }

        int low = 0, high = size - 1, mid = 0;
        while (low < high) {
            mid = (high - low) / 2 + low;
            if (mid < size - 1 && num[mid] > num[mid + 1]) {
                return num[mid + 1];
            }
            else if (mid == size - 1 && num[mid] > num[0]) {
                return num[0];
            };
            if (num[mid] >= num[low]) {
                low = mid + 1;
            }
            else {
                high = mid - 1;
            }
        }
    }

    void display(string s) {
        for (int i = 0; i<s.size(); i++) {
            cout << s[i];
        }
        cout << endl;
    }
};

